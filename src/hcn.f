        do 294 it = 1,39
            einstein(it) = 64.d0*(pi**4)
     &          *((frequency(it)/c*1.d6)**3)
     &          /(3.d0*planck)*(dpdipole**2)*relative(it)
     &          /(2.d0*dble(fq(upper(it)+1))+1.d0)
     &          *3.0d0*dble(jq(upper(it)+1))
 294        continue

c Check the Einstein As. Sum the As for all hyperfine
