        do 408 line = 1,nlines
          do 409 ihyp = 1,nhyp(line)
            it = istartline(line) + ihyp
            relint(ihyp,line) = relative(it)
            hypvel(ihyp,line) =
     &        -(frequency(it)*1.d6/frequency_line(line)-1.d0)*c
 409      continue
